package org.me.gj.model.logistica.informes;

public class NotaESSublin extends MontosTotales {

    private int slin_key;
    private String slin_id;
    private String slin_des;

    public NotaESSublin() {
        super();
    }

    public int getSlin_key() {
        return slin_key;
    }

    public void setSlin_key(int slin_key) {
        this.slin_key = slin_key;
    }

    public String getSlin_id() {
        return slin_id;
    }

    public void setSlin_id(String slin_id) {
        this.slin_id = slin_id;
    }

    public String getSlin_des() {
        return slin_des;
    }

    public void setSlin_des(String slin_des) {
        this.slin_des = slin_des;
    }

}
